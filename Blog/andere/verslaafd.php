                    <p> "Verliefd worden is niet iets wat iemand met je doet, het is iets wat je met jezelf doet"
                    </p>
                    <p>
                        Er zijn veel manieren om iemand aangetrokken tot je te maken. Seks kan een zeer krachtige aantrekkingsmethode zijn, maar
                        er zijn ook technieken in dating die je kunt gebruiken om je minnaar verslaafd aan je te maken. Ze zijn vaak gebaseerd
                        op plagen, het creëren van negatieve emoties en een intens verlangen naar het gemiste object. Wees alsjeblieft attent
                        bij het daten, je wilt niet iemands hart breken door hem aan het lijntje te houden. Seks en deze technieken zijn zeer
                        krachtig, dus je moet voorzichtig zijn met wat je wenst. Je geliefde kan hard verliefd worden, dus wil je liefde
                        wederkerig zijn. Je kunt veel dingen ten goede en ten kwade gebruiken, dus zorg ervoor dat je deze technieken gebruikt
                        voor rechtvaardige en goede doeleinden. Deze technieken kunnen overkomen als bedrieglijk of manipulatief, echter, het
                        uiteindelijke doel moet een wederzijds plezier voor jullie beiden zijn. Uiteindelijk wil je er zeker van zijn dat je
                        geliefde geen spijt zal krijgen van zijn verliefdheid.
                    </p>
                    <h2>Principes</h2>
                    <h3>Afwezigheid</h3>
                    <p>
                        We moeten eerst begrijpen wat romantische liefde is. Liefde heeft eigenlijk veel gemeen met verslaafd of geobsedeerd
                        zijn. Door dezelfde onderliggende mechanismen te gebruiken, kun je intense aantrekkingskracht en hunkering creëren. Wat
                        vaak nodig is voor liefde is afwezigheid. Als je constant bij iemand bent en alleen maar voor positieve emoties zorgt,
                        begint diegene je als vanzelfsprekend te beschouwen. Dit zal de aantrekkingskracht vrij snel beëindigen. Met afwezigheid
                        leer je wat je echt mist.
                    </p>
                    <h3>Gemengde gevoelens</h3>
                    <p>
                        Als je iemand af en toe teleurstelt, zal hij dat meer waarderen. Een mix van het geven van positieve en negatieve
                        emoties maakt je onvoorspelbaar, en contra-intuïtief waardevoller. Je kunt dit principe vergelijken met een gokmachine.
                        Als je altijd zou winnen, zou het vrij snel saai worden. Je wilt eigenlijk variatie, je wilt een variabele uitbetaling.
                        Deze anticipatie op een goede uitkomst is wat het zo verslavend maakt. Als je dan eindelijk een positieve reactie hebt,
                        geniet je er nog meer van. Niemand wil eigenlijk alleen maar positieve emoties ervaren, natuurlijk willen ze vooral
                        positieve emoties hebben. Maar wat we eigenlijk willen is een emotionele achtbaan, we willen een scala aan emoties
                        voelen.
                    </p>
                    <h3>Uitgestelde bevrediging</h3>
                    <p>
                        Stel je dit voor, je hebt 2 bazen, de ene is zorgeloos over zijn geld, de andere is een beetje hebzuchtig. Voor de
                        eerste doe je niets terwijl je toch betaald krijgt, voor de tweede werk je je uit de naad, en uiteindelijk krijg je
                        betaald. De meeste mensen zouden meer plezier halen uit het betaald krijgen door de tweede baas, dit is het principe van
                        uitgestelde bevrediging. Door te wachten of te werken voor een beloning wordt het bevredigender.
                    </p>
                    <h3>Diskwalificeren</h3>
                    <p>
                        Er zijn verschillende gradaties van diskwalificeren, je kunt iemand plagend wegduwen, of legitiem iemand wegduwen.
                        Plagen kan meer flirterig zijn, en niet zo serieus. Je zou dit vaker moeten doen, en gewoon voor de lol. Echter, om je
                        geliefde naar je te laten verlangen, kun je het een tandje hoger zetten. Je kunt je partner diskwalificeren door
                        hem/haar te vertellen waarom jullie nooit samen kunnen zijn. Dit kun je alleen doen als je elkaar al duidelijk leuk
                        vindt, het moet congruent zijn en geijkt aan de situatie.
                    </p>
                    <h2>Ontvangsten lezen</h2>
                    <p>
                        Je kunt actief niet reageren op iemands sms om je geliefde naar je te laten verlangen. Als je bijvoorbeeld heen en weer
                        hebt ge-sms't, een beetje hebt geflirt, maar niet van plan bent om je geliefde die avond op te zoeken, dan bereik je met
                        meer sms'jes niets meer. Als u terug sms't, sluit u de lus, het maakt uw geliefde comfortabel en veilig. Je geliefde
                        weet dat hij/zij je kan hebben, misschien moet je zelfs dubbel sms'en. De volgende keer begin je gewoon opnieuw met het
                        gesprek, maar je geliefde weet niet zeker of je beschikbaar bent. Een andere situatie waarin je dit kunt doen is als je
                        geliefde je een try-hard sms'je stuurt. Als je niet meteen reageert, gaat je geliefde zich zorgen maken of het over de
                        top is. Dit is eigenlijk geen slechte zaak, als je wilt dat je geliefde geobsedeerd raakt door jou. Dit plant het zaadje
                        van zijn/haar obsessie. Er is eigenlijk niets verloren door dit te doen.
                    </p>
                    <h2>Framing en storytelling</h2>
                    <p>
                        Je wilt een gemeenschappelijk liefdesverhaal creëren. Dit is het verhaal van hoe jullie beiden verliefd zijn geworden,
                        het is het verhaal van jou en je geliefde. Je wilt de hoofdrol spelen in het verhaal, maar dring het niet op. Als je
                        geliefde het verhaal aan zijn/haar vrienden vertelt, moet je het verhaal aanmoedigen. Het hoeft niet echt waar te zijn.
                        Creëer een verhaal van 'Wij tegen de wereld', en bouw de identiteit van de relatie op via de verhalen van jou en je
                        geliefde.
                    </p>
                    <h2>Freezeout</h2>
                    <p>
                        Wat de meeste mensen onder freeze out verstaan, is het straffen van je minnaar voor fouten of afwijzing. Stel dat je een
                        move maakt naar je minnaar en je vordert naar seks, maar je minnaar wijst je af. Dan moet je nooit freeze out doen, en
                        zoenen en seks volledig afwijzen, het is soms een test of je grenzen respecteert. Dit is een vorm van emotionele
                        chantage, en het is niet gezond of bevorderlijk voor de relatie. In plaats daarvan kun je de preventieve freeze out
                        gebruiken, dit is niet zo reactief of kwaadaardig, maar veel effectiever. Je trekt je terug of creëert een obstructie
                        zonder daadwerkelijk te straffen voor fouten. In seks is dit beter bekend als plagen. Dit kan een geweldig voorspel zijn
                        en bouwt anticipatie op. Een goede freeze-out is geen reactie of straf, het doel is om meer spanning, voorspel en
                        opwinding te creëren.
                    </p>
                    <h2>Opschorting van open-loop</h2>
                    <p>
                        Dit is het idee van niet weten, het creëert angst en anticipatie. Het resultaat is vaak anders dan wat je verwachtte.
                        Anticipatie is erger dan realiteit. Al die obsessie was voor niets. Je kunt je geliefde naar je laten verlangen door
                        open lussen te creëren. Door dingen onbeantwoord te laten, gaat je geliefde zich erover verwonderen. Bijvoorbeeld, als
                        je geliefde je sms't:'we moeten praten', dan gaan alle alarmbellen af. We associëren het onmiddellijk en verwachten dat
                        er iets ergs gaat gebeuren. Maar als je geliefde gewoon een reisje aan het plannen was, dan ben je gewoon geobsedeerd
                        over niets, en heb je eigenlijk heel veel plezier. De verwondering en spanning die je opbouwt zijn krachtig. Je laat
                        iemand verliefd op je worden terwijl je niet eens in de buurt bent.
                    </p>
                    <h2>Laat je partner helpen en voor je werken</h2>
                    <p>
                        We hechten veel meer waarde aan iets als we ergens voor werken. We kunnen dit principe toepassen door iemand ons te
                        laten helpen, of werk voor ons te laten doen. Zoals je waarschijnlijk al een keer hebt gemerkt, vragen vrouwen meestal
                        mannen om hulp, omdat mannen zich daardoor machtig en gewaardeerd voelen, en als je iemand helpt, begin je hem
                        automatisch aardig te vinden. Je kunt hetzelfde principe gebruiken, het werkt beide kanten op. Vraag om hulp, ook als je
                        het prima zelf kunt. Vraag vooral naar waar je geliefde in uitblinkt, vraag vrouwen om advies over mode, winkelen of
                        koken, vraag mannen om je te helpen met huiswerk, lichamelijk werk of sport. Tijdens de seks kun je ook voor elkaar
                        werken door manuele en orale seks te bedrijven. Orale en manuele seks kan enige inspanning vergen. Houd het in balans,
                        geef en ontvang. Toon je waardering terwijl je partner op je gaat liggen.
                    </p>
                    <p>
                        Zo, daar heb je het, krachtige technieken om je geliefde verslaafd aan je te maken. Onthoud dat je deze alleen voor
                        goede doeleinden moet gebruiken. Alsjeblieft, gebruik deze technieken niet voor validatie of als een ego boost. Bouw
                        sterke positieve relaties op en maak elkaar gelukkig. Deze technieken kunnen zo krachtig zijn dat je partner misschien
                        zelfs cadeautjes voor je koopt of veel werk voor je doet.
                    </p>
