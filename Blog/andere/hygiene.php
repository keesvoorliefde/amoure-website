                    <p>
                        Een van de grootste turnoffs binnen dating is dat mensen onhygienisch zijn. Hoeveel vrouwen wel niet zeuren
                        over mannenzweet en andere nare geurtjes. Hygiene hoeft helemaal niet zo'n grote taak te zijn. Het zijn simpelweg gewoontes
                        die je jezelf moet aanleren.
                        Om er voor te zorgen dat jij niet afgewezen wordt omdat je een ranzige viespeuk bent, zijn hier 8 tips.

                    </p>
                    <ol>
                        <li>
                            <h2>Douchen</h2>
                            <p>
                                Persoonlijke voorkeur kan bepalen hoe vaak u wilt douchen, maar de meeste mensen hebben baat bij minstens om de dag een douche. Of je vaak sport of veel zweet bepaalt hoe vaak je onder de douche moet. Zweet na de training kan je poriën verstoppen en olie en dode huidcellen vasthouden, wat leidt tot puistjes. Deodorant stopt de geur niet, je moet gewoon onder de douche, viespeuk.
                            </p>
                            <p>
                                Vermijd het gebruik van overmatig warm. Het is natuurlijk lekker om een warme douche te nemen, maar overdrijf niet. Heet water kan droogheid, roodheid, jeuk en zelfs vervellen veroorzaken. Douchen met zeep helpt bij het wegspoelen van dode huidcellen, bacteriën en oliën. Je moet vooral focussen op de meest stinkende gebieden, dit zijn je oksels, je billen, en je schaamstreek. Wrijf met je handen, een washandje of een spons.
                            </p>
                            <p>
                                Je moet ook je haar ten minste één of twee keer per week wassen, maar niet te vaak. Sommige mensen hoeven hun haar alleen met water te wassen, of nauwelijks met shampoo te wassen. Het wassen van je haar en hoofdhuid helpt bij het verwijderen van huidophopingen en beschermt tegen vette resten die je huid kunnen irriteren.
                            </p>
                            <p>
                                Vervang je handdoek regelmatig, minstens één keer per week. Als je een stinkende handoek gebruikt, kan je weer opnieuw de douche in. Je merkt het verschil misschien niet, maar anderen wel.
                            </p>
                        </li>
                        <li>
                            <h2>Gezichtshygiëne</h2>
                            <p>
                                Sommige mensen hebben profijt aan het dagelijks reinigen van hun gezicht, maar zeker niet iedereen. Gebruik geen zeep, bodygel of bodyscrub op je gezicht, want dat ontdoet je huid van het essentiële vocht en verstoort de natuurlijke pH-balans, waardoor je huid geïrriteerd en droog wordt. Je kunt ook gezichtsmaskers gebruiken, zo kun je mee-eters op je neus en wangen verwijderen, en soms ook op je kin en oren.
                            </p>
                        </li>
                        <li>
                            <h2>Knip en trim je haar.</h2>
                            <p>
                                Ga regelmatig naar de kapper en kies een kapsel dat bij jouw hoofd past om het mooi in stijl te houden. Als je graag gladgeschoren bent, scheer je baard dan zodra hij begint te prikken. Als je een baard hebt, trim hem dan regelmatig en maak hem goed schoon om hem te beschermen tegen vuil dat zich in de wortels nestelt.
                            </p>
                            <p>
                                Trim ook je neus en oorharen als je die hebt. Dit is meer een probleem voor oudere mannen, maar ik zou het een uitdaging vinden om iets te bedenken dat meer een onmiddellijke afknapper is. Werk ook je monobrauw bij, knip met een klein schaartje of haal het haar tussen je wenkbrauwen met een pincet weg. Als je overmatig veel okselhaar hebt, knip dat dan ook zorgvuldig af.
                            </p>
                        </li>
                        <li>
                            <h2>Mondhygiëne</h2>
                            <p>
                                Mondhygiëne is een belangrijk onderdeel van persoonlijke hygiëne en wordt gemakkelijk over het hoofd gezien. Je moet twee of drie keer per dag poetsen voor 2 minuten. Optioneel kan je flossen en één keer per dag je tong scrubben of poetsen, vooral achterin je tong. Als het niet goed wordt schoongemaakt, is het een geweldige plek voor bacteriën die zich in de mond ophopen. Reinig je mond dus goed om een slechte adem te voorkomen en gaatjes door voedselbederf tegen te gaan.
                            </p>
                        </li>
                        <li>
                            <h2>Maak je oren schoon</h2>
                            <p>
                                Veel mensen de neiging hebben om hun oren te verwaarlozen. Neem een nat washandje en haal het rond en achter je oren. Zorg ervoor dat je het vuil dat rond je oren zit opveegt. Gebruik een wattenstaafje om het oorsmeer weg te halen
                            </p>
                        </li>
                        <li>
                            <h2>Handen wassen</h2>
                            <p>
                                Het hoort eigenlijk niet gezegd te worden, maar was je handen na een wc bezoek of voor het koken. Scrub met zeep gedurende 20 tot 30 seconden, en zorg ervoor dat je tussen je vingers, op de rug van je handen en onder je nagels reinigt. Als je geen stromend water of zeep hebt, kan je ook handdesinfectans op alcoholbasis gebruiken.
                            </p>
                        </li>
                        <li>
                            <h2>Knip je nagels</h2>
                            <p>
                                Net als vrouwen hun nagels verzorgen, moeten ook mannen hun nagels schoon en kort houden.
                                Knip je nagels regelmatig om ze kort en schoon te houden. Borstel eronder met een nagelborsteltje of washandje om vuil en bacteriën weg te spoelen. Vermijd ook het bijten op je nagels, lees <a href="https://www.huisarts.nl/nagelbijten-hoe-kom-je-ervan-af/">hier</a> hoe je er van af komt.
                                Lange teennagels zien er slordig uit en zijn ook ongezond. Het is een van de belangrijkste oorzaken van stinkende voeten. Als je teennagels vuil beginnen op te hopen, is dat een teken dat je ze moet knippen.
                                Door je nagels schoon te houden voorkom je dat ziektekiemen verspreiden.
                            </p>
                        </li>
                        <li>
                            <h2>Draag geen ongewassen kleding of sokken</h2>
                            <p>
                                Verwissel je sokken en ondergoed bijna elke dag, anders begint het echt te stinken. Je kunt een goed wasmiddel gebruiken om uw kleding en ondergoed te wassen, zodat je elke dag fris en fruitig ruikt. Draag ook je shirt niet langer dan 2 dagen, anders begint je standaard naar zweet te stinken.
                                Gewone truien en broeken kan je zeker vijf keer dragen voor ze gewassen moeten worden. Was ze als er vlekken in komen met een washandje, en lucht ze om nare geuren te voorkomen.
                            </p>
                        </li>
                        <li>
                            <h2>Geslachtsdeel hygiëne</h2>
                            <p>
                                Seksuele hygiëne is even gevoelig en belangrijk voor mannen als voor vrouwen.
                                Iedereen moet zijn schaamhaar trimmen, schoonmaken en goed behandelen. Lang haar daar beneden kan je een ongemakkelijk gevoel geven en overmatig zweet veroorzaken, wat ongezond en besmettelijk is. Was je geslachtsdeel dagelijks, of na de seks met lauw water en dep het voorzichtig droog. Was vooral de buitenkant, om je vaginale flora te behouden.
                            </p>
                            <p>
                                Verwissel je tampons en maandverband om de vier uur als je ongesteld bent. En bezoek het toilet na de seks, dit voorkomt besmettingen.
                            </p>
                        </li>
