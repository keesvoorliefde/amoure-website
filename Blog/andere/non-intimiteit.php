                    <h2>Casual seks</h2>
                    <p>
                        Hetzelfde principe geldt voor casual seks. Casual seks wordt vaak in een negatief daglicht gezien, maar het kan juist
                        versterkend en leuk zijn. Er zijn een heleboel redenen waarom casual seks voor vrouwen minder bevredigend is dan voor
                        mannen. Het is vaak omdat je geen intimiteit kunt opbouwen en je dominantie niet kunt ontketenen bij een one night
                        stand. Het is goed om te onthouden dat zelfs als je geen relatie wilt, intimiteit nog steeds belangrijk is. Je zou nog
                        steeds gesprekken, knuffels en kussen vooraf moeten hebben, ook al zit je niet in een relatie.
