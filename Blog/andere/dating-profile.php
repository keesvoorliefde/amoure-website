                    <p>
                        Hoewel niet zo belangrijk als de foto's, kan je persoonlijke beschrijving de deal breken of maken. Het werkt vooral goed
                        als je een gedeelde interesse hebt of voor mensen die op zoek zijn naar specifieke kenmerken.
                    </p>
                    <ol>
                        <li>
                            <h3>Wees zo kernachtig mogelijk.</h3>
                            <p>
                                Als je maar een paar zinnen ruimte hebt, telt alles. Je profiel moet rijk zijn aan inhoud, omdat het zo kort is.
                                Verzamel een aantal beschrijvende woorden die je vastleggen, zoals "Hou van koken. Leraar. Kattenliefhebber". Je wilt er
                                zoveel mogelijk informatie in stoppen. Misschien wil je wat oppervlakkige kenmerken van jezelf toevoegen, zoals lengte
                                en gewicht. Zorg ook voor een duidelijk nederlandse taal, dit is anders een afknapper.
                            </p>
                        </li>
                        <li>
                            <h3>Zorg voor een converserende opening.</h3>
                            <p>
                                Laat zien dat je gevoel voor humor hebt, als je iemand aan het lachen kunt maken, is dat een geweldige ijsbreker en kan
                                je gesprek een geweldige start hebben. Of daag iemand uit in je beschrijving, zoals "Ik wed dat je me niet kunt verslaan
                                met Mario Kart". Vertel wat over jezelf, zodat mensen kunnen bepalen of jullie iets wezenlijks gemeen hebben.
                            </p>
                        </li>
                        <li>
                            <h3>Blijf positief terwijl je specifiek bent over waar je naar op zoek bent.</h3>
                            <p>
                                Het is beter om te focussen op het positieve, in plaats van een negatieve disclaimer, zoals "Bericht me niet als je
                                alleen maar wilt afspreken" . Allereerst, schrijf je in op sites die specifiek voor jouw niche bedoeld zijn, waar je
                                meer ruimte hebt om uit te leggen wie je bent en wat je wilt. Laat zien wat voor activiteiten en ervaringen jullie samen
                                willen hebben, het maakt mensen erop attent dat jullie de juiste match zijn, en het schrikt mensen af die dat niet zijn.
                            </p>
                            <p>
                                Het is belangrijk dat je niet te hoge eisen stelt, want dan kun je te kritisch overkomen, zelfs bij iemand die aan alle
                                voorwaarden voldoet. Vaak worden mensen verliefd op iemand die niet hun type is. Je wilt een beetje ruimte maken voor de
                                magie die op je pad komt. Je moet meerdere mensen daten om erachter te komen wat je leuk vindt.
                            </p>
                        </li>
