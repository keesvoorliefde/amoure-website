                            <h3>Vermijd clichés.</h3>
                            <p>
                                Maak je profiel uniek, en vermijd typische clichés. Neem geen toevlucht tot uitspraken als, "Ik weet niet echt wat ik
                                hier moet schrijven" of "Ik hou van reizen,". Benadruk in plaats daarvan wat jou anders maakt dan alle anderen. Als je
                                genoeg ruimte hebt, kun je drie korte alinea's schrijven, twee over jezelf, en één over wat je zoekt. In plaats van
                                algemene activiteiten, maak het specifieker, zoals, "ik ben net terug van een heerlijke reis naar Thailand", of "ik hou
                                ervan om met mijn hond te knuffelen, maar ook met jou".
