                    <h2>Seks hebben in risicovolle situaties</h2>
                    <p>
                        Voor de eerste keer met een nieuwe partner zou risicovolle seks verkeerd zijn, je moet eerst vertrouwen opbouwen. Maar
                        in een langdurige relatie helpt het om de druk te bestrijden. Je wilt het eigenlijk wel veilig doen, maar de illusie
                        creëren van risico en spanning. Je moet seks hebben die je zeer beloont, maar die niets of weinig kost. Er zijn
