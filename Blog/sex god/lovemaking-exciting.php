                        In langdurige relaties hebben mensen vaak geen actief seksleven meer. Ze voelen geen rauwe aantrekkingskracht tot
                        elkaar, en sommigen gaan zelfs vreemd. Als je helemaal niets doet wordt seks en daarmee de relatie steeds saaier, dus
