                        De tweede valkuil in een langdurige relatie is dat er impliciete druk is om seks te hebben. Het lijkt erop dat het voor
                        jullie beiden het rationele is om te doen. En dan hoor ik :"maar seks bevordert onze relatie, het is gezond en leuk".
                        Hoewel dit klopt, is de rationele druk verschrikkelijk. Mensen zijn geen logische wezens, we zijn emotionele wezens. We
                        willen wanhopig vrij zijn, en niet opgesloten worden in ons leven.
                    </p>

                    <h2>Verleg je grenzen</h2>
                    <p>
