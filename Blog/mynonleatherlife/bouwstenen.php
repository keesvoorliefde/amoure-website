                    <h2>Patriarchaat</h2>
                    <p>
                        Nog niet zo lang geleden werden vrouwen gezien als inferieur aan mannen. Ze hadden veel minder rechten en vrijheid dan
                        mannen. Mannen bezitten vrouwen in principe en zien hen als eigendom. Veel onderdanige en dominante fantasieën passen
                        onder deze paraplu. Patriarchaat is een door mannen gedomineerde wereld waarin mannen heersen en zich superieur voelen
                        aan vrouwen. Deze fantasie is te zien in erotiek, of erotische films, enkele voorbeelden zijn een elite club van mannen
                        in het verhaal van O, een directiekamer zoals in 50 shades of grey, een sci-fi planeet geregeerd door mannen, of een
                        door mannen gedreven zakenwereld of de maffia familie in 365 DNI. Je kunt dit begrijpen, want welke man zou niet in een
                        kamer met hete vrouwen willen zijn. Het is het gevoel omringd te zijn door mannelijkheid.
                    </p>
                    <h2>Hypervrouwelijkheid</h2>
                    <p>
                        Vrouwen worden gezien, gewaardeerd en verhandeld om hun schoonheid en vrouwelijkheid, niet om hun intelligentie of
                        persoonlijkheid. Dat is vaak een gevoel van schending. Jongens willen vaak gezien worden om hun mannelijke kwaliteiten.
                        Hypervrouwelijke eigenschappen die overkomen als zwakheden worden vaak benadrukt, zoals verlegenheid, naïviteit,
                        gevoeligheid, of verlegenheid. Veel verschillende veel voorkomende vrouwelijke fantasieën en kinks passen onder deze
                        paraplu van hypervrouwelijkheid.
                    </p>
                    <h2>Oerfokrechten</h2>
                    <p>
                        De man moet de vrouw zijn oerfokrechten laten zien. Het is een wedstrijd voor de beste man. Hij moet zijn krachtfeiten
                        tonen, een test van kracht, wil, intelligentie, leiderschap, agressie. Hij moet bewijzen dat hij de sterkste is, de
                        fitste, en het meest waardig voor voortplanting en het recht om vader te zijn. Hij moet het verdienen door zijn
                        geschiktheid te bewijzen. Als hij dat niet doet, kan zij nooit tevreden met hem zijn, want zij zoekt de man die het
                        meest geschikt is om te overleven. Dit stelt vrouwen in staat mannen te beoordelen die het waard zijn hun eigen lichaam,
                        geest en ziel te geven. Veel oer-fantasieën passen onder deze paraplu van het kijken naar een man die zijn evolutionaire
                        fitheid aan jou bewijst.
