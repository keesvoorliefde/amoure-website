                    <p>
                        Als je afgewezen wordt kun je jezelf voor je kop slaan, je kunt denken dat je afgewezen bent omdat je lelijk bent,
                        gebrekkig, arm, etc. Er zijn echter een heleboel redenen waar je misschien niet aan denkt die verklaren waarom een vrouw
                        je afwijst die veel waarschijnlijker zijn. Je moet de vrouwelijke geest begrijpen, je moet de echte reden begrijpen
                        waarom ze je afwijzen. Anders zul je nooit verbeteren, je zult altijd dezelfde fouten maken, keer op keer. Je achting
                        kan dalen, simpelweg omdat je denkt dat je bent afgewezen om redenen die in werkelijkheid niet waar zijn.
                    </p>
                    <h2>Angst voor mannen</h2>
                    <p>
                        Je hebt geen idee hoe beangstigend de gedachte aan een simpele date of een ontmoeting alleen met een man is op een
                        onderbewust niveau. De meeste vrouwen hebben een diepe angst voor mannen, simpelweg omdat ze vaak het zwakke geslacht
                        zijn. Ze kunnen je afwijzen omdat ze op dat moment niet dapper genoeg zijn om een ontmoeting te riskeren. Vrouwen zijn
                        voortdurend op hun hoede voor gevaarlijke mannen, ze moeten in hun dagelijks leven maatregelen nemen om zichzelf te
                        beschermen. Vrouwen zien letterlijk elke man als een potentiële moordenaar en verkrachter. Vrouwen voelen vaak je agenda
                        van verlangen naar seks van hen, en dat kan zeer ongemakkelijk zijn, omdat vrouwen zich vaak schamen over hun eigen
                        seksualiteit. Dit is ook de reden waarom vrouwen mannen hun nummer geven, ze zijn misschien bang dat je boos wordt of
                        alleen maar seks wilt hebben. Misschien is ze wel geïnteresseerd, maar nog steeds bang om nieuwe mensen te ontmoeten die
                        gevaarlijk kunnen zijn. Je moet eerst genoeg vertrouwen winnen, je moet laten zien dat ze je kan vertrouwen. Ze moet
                        zich veilig voelen bij jou. Je moet haar laten zien, dat je haar ziet als een persoon, en niet als een stuk vlees. Maar,
                        sta ook in contact met je mannelijke essentie en verlangens. Het is prima om seks te willen, beide seksen willen en
                        hebben seks nodig. Dus, je moet je verlangen op een positieve manier communiceren.
                    </p>
                    <h2>Ze voelt druk om "eye candy" te daten om indruk te maken op haar vrienden</h2>
                    <p>
                        Veel vrouwen zullen je niet eens overwegen als je niet knap genoeg bent, dit is vaak alleen omdat ze niet zeker genoeg
                        is om haar eigen voorkeuren te laten gelden en op te komen tegen veroordelende leeftijdsgenoten waar ze mee omgaat. Dit
                        is triest, maar waar. Je moet dit persoonlijk opvatten, want veel vrouwen zijn erg onzeker, meer dan je misschien denkt.
                        Ze voelen zich vaak onder druk gezet om indruk te maken op hun vriendinnen, zodat vriendinnen denken dat ze zelf knap
                        genoeg is om een knappe vent te krijgen. Natuurlijk zijn er een aantal dingen die je kunt doen om je uiterlijk te
                        veranderen. Je moet aan je stijl werken, want je kleren laten je niet cool genoeg lijken. Ze kan aanvoelen dat je te
                        verschillend bent van haar, en dat je niet gaat passen in haar levensstijl, haar cultuur, en vrienden. Je kleren zien er
                        misschien goedkoop uit, en niet stijlvol. Je moet met je kleding die voorzienende en beschermende vibe uitstralen.
                        Echter, sommige vrouwen willen met je uitgaan, zelfs als je er niet zo goed uitziet, omdat je andere kwaliteiten te
                        bieden hebt.
                    </p>
                    <h2>Je hebt niet echt om haar nummer gevraagd.</h2>
                    <p>
                        Je hebt gewoon een gesprek gehad en toen je niet sterk genoeg signalen van haar kreeg ben je gewoon weggegaan zonder het
                        te vragen. Dit is een grote fout die mannen maken, zelfs als je niet genoeg signalen krijgt, vraag het gewoon. Wacht
                        niet tot ze flirt want niet alle meisjes willen de agressor zijn en zullen zich zo passief mogelijk gedragen. Als ze
                        flirten, flirten ze alleen met passieve middelen. Zoals met hun haar krullen, om je hulp vragen, je complimenteren, over
                        jongens praten, etc. Veel jongens hebben een leuk gesprek met een meisje, en dan vergeten ze om het nummer van het
                        meisje te vragen. Ze moest maar eens gaan, en het gesprek is afgelopen. Het is beter om het gewoon te vragen, ze heeft
                        in haar hoofd al besloten of je het waard bent. Vaak zul je het meisje nooit meer zien, als je haar niet mee uit vraagt.
                        Vrouwen zullen je niet haten als je ze mee uit vraagt, ze voelen zich vaak aanbeden. Zoek nooit naar intense interesse,
                        misschien is ze nog niet zo in je geïnvesteerd, dus vraag het gewoon.
                    </p>
                    <h2>Je komt te "suave" over</h2>
                    <p>
                        Als je te suave overkomt, laat je haar denken dat je een amateur bent die pick-up moest studeren om met vrouwen te
                        praten. Het is altijd beter om authentiek te zijn, en echt te praten. Gebruik geen goedkope openingszinnen en probeer je
                        niet zelfverzekerder voor te doen dan je bent. Als je nerveus bent, wees dan zelf nerveus. Als je haar begeert, zeg het
                        haar. Vaak zetten mannen een valse façade op, en vertellen vrouwen alleen dat ze mooi zijn zonder het te menen. Het
                        zorgt er paradoxaal genoeg voor dat je onzeker en nep overkomt. Het is veel beter om gewoon oprecht en natuurlijk te
                        zijn. Wees niet bang voor je eigen verlangens en interesses. Een geweldige spreker en auteur over dit onderwerp is Zac
                        Perrion, hij schreef "het albasten meisje". Deze video illustreert waar het echt om gaat.
                    </p>
                    <h2>Je hebt met haar gerommeld, maar haar niet echt mee uit gevraagd</h2>
                    <p>
                        Dus je hebt haar nummer, en je begint heen en weer te smsen. Ze lacht veel, en stuurt grappige sms'jes terug. Je begint
                        te denken dat ze je echt leuk vindt. Maar het gesprek eindigt, en je sms't nooit meer. Meisjes wachten waarschijnlijk op
                        jou om haar uit te vragen. Je hoeft niet meer en meer te sms'en om haar aantrekkingskracht te winnen, of ervoor te
                        zorgen dat ze zich op haar gemak voelt bij jou. Je moet stoppen met het vermijden van afwijzing. Sms haar een paar
                        berichten, en vraag haar dan gewoon mee uit. Je verspilt alleen je tijd door zoveel te sms'en, je zult haar niet kunnen
                        leren kennen.
                    </p>
                    <h2>Je bent kleiner dan haar</h2>
                    <p>
                        Je bent misschien korter dan haar, en dat kan ervoor zorgen dat ze zich onzeker voelt over het feit dat ze zich
                        vrouwelijk voelt. Ze wil het gevoel hebben dat zij het meisje in de relatie is. Vat het alsjeblieft niet persoonlijk op,
                        dat is gewoon hoe sommige meisjes zich voelen. Je kunt een meisje zoeken dat zekerder is of uitgaan met meisjes die
                        kleiner zijn dan jij.
