                    <h2>Het E.A.R. model</h2>
                    <p>
                        Het E.A.R. model van David Burns beschrijft 3 sleutelelementen van communicatie. Deze gids zal u helpen om te gaan met
                        conflicten in uw dagelijks leven.
                    </p>
                    <p>

                        "E" staat voor "empathie", proberen het standpunt van de ander te begrijpen.
                    </p>
                    <p>
                        "A" staat voor "assertiviteit", het uitleggen van onze eigen gedachten en gevoelens op een duidelijke en directe manier.
                    </p>
                    <p>
                        "R' staat voor 'respect', oprechte zorg en bezorgdheid tonen.
                    </p>

                    <h2>De vijf geheimen</h2>
                    <p>
                        De vijf geheimen van effectieve communicatie zijn gebaseerd op het E.A.R.-kader. Het gebruik van E.A.R. en de vijf
                        geheimen is afgeleid van de vooronderstelling dat om verandering in een relatie te creëren we moeten beginnen met
                        onszelf te veranderen in plaats van te wachten tot de ander verandert, wat wel of niet kan gebeuren. Dit zijn de
                        technieken:</p>

                    <h3>Ontwapeningstechniek:</h3>
                    <p>
                        Dit betekent dat je een kern van waarheid vindt in wat de ander zegt, ook al lijkt het onredelijk, onnauwkeurig, of
                        oneerlijk. U laat de ander weten dat u het ziet. Wat dit doet is voorkomen dat een conflict ontstaat of escaleert. Stel
                        bijvoorbeeld dat je vriend zegt: "Je bent altijd te laat." Om de uitspraak te ontwapenen zou je kunnen reageren met: "Je
                        hebt gelijk, ik ben vaak te laat. Ik kan zien dat dit erg vervelend voor je is." Doordat ik het eens ben met de
                        uitspraak van mijn vriend en zijn standpunt in deze situatie zie, zal hij waarschijnlijk niet geneigd zijn om te blijven
                        klagen, wat een ruzie kan helpen voorkomen. Het is wel belangrijk om oprecht te zijn. Het gaat er niet om het met elkaar
                        eens te zijn.
                    </p>
                    <h3>Empathie in gedachten en gevoelens:</h3>
                    <p>
                        Gedachte-empathie oefen je door samen te vatten wat de ander net heeft gezegd. Je kunt exact dezelfde woorden herhalen.
                        Gevoel empathie kun je oefenen door je te verbinden met de emoties achter de boodschap, je erkent hoe hij of zij zich
                        waarschijnlijk voelt. Bijvoorbeeld: je zus zegt: "mijn vriend heeft het net uitgemaakt, ik voel me zo verdrietig". Je
                        reactie kan zijn: "Ik begrijp het, ik kan me voorstellen dat je je verdrietig voelt omdat je vriend het net heeft
                        uitgemaakt. Nogmaals, het gaat om oprecht contact maken met iemand.
                    </p>
                    <h3>"Ik voel" uitspraken:</h3>
                    <p>
                        Je drukt je eigen gevoelens en ideeën uit door middel van een "Ik voel" verklaring. Een typisch formaat om te gebruiken
                        zou kunnen zijn: "Ik voel me X als Y omdat Z. Bijvoorbeeld: "Ik voel me aangevallen als je met de deur slaat." Je moet
                        je gevoelens uitdrukken in plaats van je gedachten en de uitspraken mogen geen aanval zijn op de andere persoon. Er
                        worden geen echte gevoelens geuit. "Ik voel" uitspraken moeten emoties bevatten zoals opgewonden, geïrriteerd,
                        verdrietig, teleurgesteld, verward, verraden, nerveus, in verlegenheid gebracht, niet gerespecteerd, enz.
                    </p>
                    <h3>Strelen:</h3>
                    <p>
                        Strelen gaat over het overbrengen dat zijn of haar meningen en gevoelens net zo geldig zijn als die van onszelf. Dit
                        betekent dat we geen neerbuigende opmerkingen maken en echt naar de ander luisteren. Strelen is oprechte bezorgdheid,
                        zorg en respect tonen. Bijvoorbeeld: "Ik waardeer onze relatie echt en wil graag dat we ons probleem oplossen" Strelen
                        kan ook in de vorm van affirmaties of complimenten.
                    </p>
                    <h3>Onderzoeken:</h3>
                    <p>
                        Inquiry gaat over het stellen van vragen om de gedachten en gevoelens van de ander te verduidelijken of gewoon om meer
                        te weten te komen. Het is een manier om uit te nodigen tot verdere discussie, zodat u de kwestie adequaat kunt
                        aanpakken. Het is belangrijk dat u niet probeert om hun probleem voor hem/haar op te lossen, u moet eerst luisteren.
                        Geef alleen concreet advies als de ander er om vraagt, je kunt vragen of je advies mag geven.
                    </p>
                    <h2>Combinaties van de geheimen</h2>
                    <p>
                        Mensen gebruiken de 5 geheimen niet op dagelijkse basis. In een conflict is het echter wel zinvol om de 5 geheimen te
                        gebruiken. De 5 geheimen moeten in combinatie gebruikt worden, dat is wat ze effectief maakt. Het hangt van de situatie
                        af welke combinatie je gebruikt. In veel gevallen lost alleen het gebruik van empathie en aaien het conflict op. Het
                        belangrijkste is om de technieken op een natuurlijke en oprechte manier te gebruiken, het helpt je om je authentieke
                        zelf te uiten. De andere persoon kan dwars door je heen kijken als je nep doet. Ze kunnen meer van streek raken en
                        denken dat je hen manipuleert of ongeldig maakt. Als je deze technieken oefent, zul je geleidelijk aan steeds beter
                        worden. Je kunt dit implementeren door je elke week op elke techniek te concentreren. Schrijf in je dagboek over een
                        specifieke situatie, en hoe je de techniek had kunnen gebruiken om het conflict op te lossen.
                    </p>
